package com.dream.ccms.controller.request;

public class CoalRequest {

	String coalName;
	String coalCode;
	String price;
	String ad;
	String vdaf;
	String s;
	String g;
	String y;
	String re;
	public String getCoalName() {
		return coalName;
	}
	public void setCoalName(String coalName) {
		this.coalName = coalName;
	}
	public String getCoalCode() {
		return coalCode;
	}
	public void setCoalCode(String coalCode) {
		this.coalCode = coalCode;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public String getVdaf() {
		return vdaf;
	}
	public void setVdaf(String vdaf) {
		this.vdaf = vdaf;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public String getG() {
		return g;
	}
	public void setG(String g) {
		this.g = g;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getRe() {
		return re;
	}
	public void setRe(String re) {
		this.re = re;
	}
	
	
}
